#include <unistd.h>

void	ft_initiate_array(char *arr)
{	
	arr[0] = '0';
	arr[1] = '0';
	arr[2] = ' ';
	arr[3] = '0';
	arr[4] = '0';
}

void	ft_print_comb2(void)
{
	char	n[5];

	ft_initiate_array(n);
	while (n[0] != '9' || n[1] != '8' || n[3] != '9' || n[4] != '9')
	{
		n[4]++;
		if (n[4] > '9')
		{
			n[4] = '0';
			n[3]++;
		}
		if (n[3] > '9')
		{
			n[3] = '0';
			n[1]++;
		}
		if (n[1] > '9')
		{
			n[1] = '0';
			n[0]++;
		}
		write(1, &n, 5);
		write(1, ", ", 2);
	}		
}
