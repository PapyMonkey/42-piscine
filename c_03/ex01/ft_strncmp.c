int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	n_count;

	n_count = 1;
	if (n == 0)
		return (0);
	while (n_count < n && *s1 && *s2 && *s1 == *s2)
	{
		s1++;
		s2++;
		n_count++;
	}
	return (*s1 - *s2);
}
