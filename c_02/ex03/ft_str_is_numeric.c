int	ft_str_is_numeric(char *str)
{
	int	flag;

	flag = 1;
	while (*str != '\0')
	{
		if (!(48 <= *str && *str <= 57))
		{
			flag = 0;
			break ;
		}
		str++;
	}
	return (flag);
}
